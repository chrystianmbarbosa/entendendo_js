// Faça um algoritmo que peça dois números e imprima o maior deles.

let numero1 = parseInt(prompt("Informe o Primeiro Numero:"));
let numero2 = parseInt(prompt("Informe o Segundo Numero:"));

if (numero1 > numero2) {
    alert("O numero 1 é maior que numero 2.");
} else {
    alert("O numero 2 é maior que numero 1.");
}


